function buildGraph() {
  function getW(b1, b2, x) {
    if (x) {
      const w1 = b1.x2 - b1.x1
      const w2 = b2.x2 - b2.x1
      return (w1 + w2) / 2
    }
    else {
      const h1 = b1.y2 - b1.y1
      const h2 = b2.y2 - b2.y1
      return (h1 + h2) / 2
    }
  }
  Object.entries(graph).forEach(([id, block]) => {
    const { x1, x2, y1, y2 } = block
    let adj_id
    if (x1 - 2 > -1 && grid[y1][x1 - 1] === -1 && (adj_id = grid[y1][x1 - 2]) < -1) {
      block.adj.push({ id: adj_id, w: getW(block, graph[adj_id], true) })
    }
    if (x2 + 2 < m && grid[y1][x2 + 1] === -1 && (adj_id = grid[y1][x2 + 2]) < -1) {
      block.adj.push({ id: adj_id, w: getW(block, graph[adj_id], true)})
    }
    if (y1 - 2 > -1 && grid[y1 - 1][x1] === -1 && (adj_id = grid[y1 - 2][x1]) < -1) {
      block.adj.push({ id: adj_id, w: getW(block, graph[adj_id], false) })
    }
    if (y2 + 2 < n && grid[y2 + 1][x1] === -1 && (adj_id = grid[y2 + 2][x1]) < -1) {
      block.adj.push({ id: adj_id, w: getW(block, graph[adj_id], false) })
    }
  })
}
