function check(r, c) {
  if (grid[r][c] <= 0) return
  const n_adj =
    (grid[r][c + 1] > 0) +
    (grid[r][c - 1] > 0) +
    (grid[r - 1][c] > 0) +
    (grid[r + 1][c] > 0)
  if (n_adj > 2) {
    return
  }
  if (
    n_adj === 2 &&
    ((grid[r][c + 1] > 0 && grid[r][c - 1] > 0) ||
      (grid[r - 1][c] > 0 && grid[r + 1][c] > 0))
  ) {
    return
  }
  if (grid[r][c + 1] === 0) {
    for (let i = c + 1; i < m; i++) {
      if (grid[r][i] <= 0) {
        grid[r][i] = -1
      } else {
        break
      }
    }
  }
  if (grid[r][c - 1] === 0) {
    for (let i = c - 1; i > -1; i--) {
      if (grid[r][i] <= 0) {
        grid[r][i] = -1
      } else {
        break
      }
    }
  }
  if (grid[r + 1][c] === 0) {
    for (let i = r + 1; i < n; i++) {
      if (grid[i][c] <= 0) {
        grid[i][c] = -1
      } else {
        break
      }
    }
  }
  if (grid[r - 1][c] === 0) {
    for (let i = r - 1; i > -1; i--) {
      if (grid[i][c] <= 0) {
        grid[i][c] = -1
      } else {
        break
      }
    }
  }
}

